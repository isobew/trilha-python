dados = {"nome": "Isabella", "idade": 22, "telefone": "3333-1234"}

print(dados["nome"])  # "Isabella"
print(dados["idade"])  # 22
print(dados["telefone"])  # "3333-1234"

dados["nome"] = "Maria"
dados["idade"] = 18
dados["telefone"] = "9988-1781"

print(dados)  # {"nome": "Maria", "idade": 18, "telefone": "9988-1781"}
